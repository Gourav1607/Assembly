.model small
.486
.STACK 20H
.DATA 
INP    DB "ENTER CHAR:   $"
OUP  DB "YOUR ENTERED CHAR: $"
OUP1  DB "BINARY EQUIVALENT CHAR: $"

.CODE
.STARTUP
MOV DX,OFFSET INP
MOV AH,09H
INT 21H

MOV AH,01H
INT 21H

MOV BL,AL

MOV DL,0AH
MOV AH,02H
INT 21H

MOV DX,OFFSET OUP
MOV AH,09H
INT 21H

MOV DL,BL
MOV AH,02H
INT 21H

MOV DL,0AH
MOV AH,02H
INT 21H


MOV DX,OFFSET OUP1
MOV AH,09H
INT 21H

MOV CX,08H
BEGIN:TEST BL,80H
JNZ LABEL1
    MOV DL,30H
    MOV AH,02H
    INT 21H
    JMP NXT

LABEL1: MOV DL,31H
    MOV AH,02H
    INT 21H
NXT:   SHL  BL,1
LOOP BEGIN

.EXIT
END